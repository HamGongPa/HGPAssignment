#두 수의 합
def solution1(num1, num2):
    answer = num1+num2
    return answer

#두 수의 차
def solution2(num1, num2):
    answer = num1-num2
    return answer

#두 수의 곱
def solution3(num1, num2):
    answer = num1*num2
    return answer

#두 수의 나누기
def solution4(num1, num2):
    answer = num1/num2*1000
    return int(answer)

#몫 구하기
def solution5(num1, num2):
    answer = num1//num2
    return answer

#나머지 구하기
def solution6(num1, num2):
    answer = num1%num2
    return answer

